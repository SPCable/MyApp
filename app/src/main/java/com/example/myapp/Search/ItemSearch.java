package com.example.myapp.Search;

public class ItemSearch {

    String name;
    String img;

    public ItemSearch(String name, String img) {
        this.name = name;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public ItemSearch()
    {

    }
}
